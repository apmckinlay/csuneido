#include "gc.h"

#define ALLOCA_LIMIT (64 * 1024) // 64 kb

// WARNING: n is evaluated multiple times
// WARNING: result is assumed to not contain pointers (for GC)
#define tmpalloc(n) ((n < ALLOCA_LIMIT) ? (char*) _alloca(n) : new(noptrs) char[n])

